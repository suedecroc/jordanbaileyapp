const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;
const canTilt = !reduceMotion && window.matchMedia("(pointer: fine)").matches;
const shouldRevealAll = Boolean(window.location.hash);
const pageDescription = document.querySelector('meta[name="description"]');
const languageButtons = document.querySelectorAll("[data-lang-switch]");
const storageKey = "jb-site-language";

const getStoredLocale = () => {
  try {
    return localStorage.getItem(storageKey);
  } catch {
    return null;
  }
};

const setStoredLocale = (locale) => {
  try {
    localStorage.setItem(storageKey, locale);
  } catch {
    // Ignore storage failures in private browsing contexts.
  }
};

document.querySelectorAll("[data-email-link]").forEach((link) => {
  link.href = `mailto:${profile.email}`;
});

document.querySelectorAll("[data-email-text]").forEach((node) => {
  node.textContent = profile.email;
});

document.querySelectorAll("[data-instagram-link]").forEach((link) => {
  link.href = profile.instagramUrl;
});

document.querySelectorAll("[data-instagram-text]").forEach((node) => {
  node.textContent = profile.instagramHandle;
});

const yearNode = document.querySelector("[data-year]");
if (yearNode) {
  yearNode.textContent = new Date().getFullYear();
}

const applyTranslations = (locale) => {
  const activeLocale = translations[locale] ? locale : "en";
  const dictionary = translations[activeLocale];

  document.documentElement.lang = activeLocale;
  document.title = dictionary.pageTitle;
  if (pageDescription) {
    pageDescription.setAttribute("content", dictionary.pageDescription);
  }

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((node) => {
    const key = node.dataset.i18nAlt;
    if (dictionary[key]) {
      node.setAttribute("alt", dictionary[key]);
    }
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
    const key = node.dataset.i18nAriaLabel;
    if (dictionary[key]) {
      node.setAttribute("aria-label", dictionary[key]);
    }
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.langSwitch === activeLocale;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  setStoredLocale(activeLocale);
};

const initialLocale = getStoredLocale() || "en";
applyTranslations(initialLocale);

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyTranslations(button.dataset.langSwitch);
  });
});

if (!reduceMotion && !shouldRevealAll) {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.16,
      rootMargin: "0px 0px -8% 0px",
    }
  );

  document.querySelectorAll(".reveal").forEach((node) => {
    const bounds = node.getBoundingClientRect();
    const isAboveFold = bounds.top < window.innerHeight * 0.92;

    if (isAboveFold) {
      node.classList.add("is-visible");
      return;
    }

    observer.observe(node);
  });
} else {
  document.querySelectorAll(".reveal").forEach((node) => {
    node.classList.add("is-visible");
  });
}

if (canTilt) {
  document.querySelectorAll(".tilt-card").forEach((card) => {
    card.addEventListener("pointermove", (event) => {
      const bounds = card.getBoundingClientRect();
      const relativeX = (event.clientX - bounds.left) / bounds.width;
      const relativeY = (event.clientY - bounds.top) / bounds.height;
      const rotateY = (relativeX - 0.5) * 8;
      const rotateX = (0.5 - relativeY) * 8;

      card.style.setProperty("--rx", `${rotateX}deg`);
      card.style.setProperty("--ry", `${rotateY}deg`);
      card.style.setProperty("--mx", `${relativeX * 100}%`);
      card.style.setProperty("--my", `${relativeY * 100}%`);
    });

    card.addEventListener("pointerleave", () => {
      card.style.setProperty("--rx", "0deg");
      card.style.setProperty("--ry", "0deg");
      card.style.setProperty("--mx", "50%");
      card.style.setProperty("--my", "50%");
    });
  });
}
